/// Iterate the children of a job-bearing block (e.g. GitHub's
/// `jobs:`) and tag each entry's value-mapping as `construct=job`
/// with a synthesised name carrier. Used when the platform nests
/// its jobs one level deep under a known key.
pub fn tag_block_entries_as_job(graph: &mut TypedGraph, block_value_id: GhostId) {
    tag_map_entries_as_construct(graph, block_value_id, "job");
}

/// Iterate a `map<construct>` block (`services: {main: {...}}`) and tag each
/// entry's value-mapping as `construct=<construct>`, synthesising a name
/// carrier from the entry key (the map key IS the instance's identity).
/// Generalises [`tag_block_entries_as_job`]: a `map<X>` field nests its X
/// instances one level deep, so the construct tag belongs on the INNER
/// value, never on the outer map wrapper (which has no construct).
pub fn tag_map_entries_as_construct(
    graph: &mut TypedGraph,
    block_value_id: GhostId,
    construct: &str,
) {
    let entries: Vec<(GhostId, String, usize, usize)> = graph
        .iter_edges()
        .into_iter()
        .filter(|(s, _, e)| *s == block_value_id && e.type_id == CST_HAS_CHILD)
        .filter_map(|(_, t, _)| {
            let node = graph.get_node(&t)?;
            if node.type_id != CST_MAPPING_ENTRY {
                return None;
            }
            let key = node.attrs.get(cst_attr::KEY)?.clone();
            let s_start = node.attrs.get(cst_attr::SPAN_START)?.parse().ok()?;
            let s_end = node.attrs.get(cst_attr::SPAN_END)?.parse().ok()?;
            Some((t, key, s_start, s_end))
        })
        .collect();

    for (entry_id, key, s_start, s_end) in entries {
        let value_id_opt = graph
            .iter_edges()
            .into_iter()
            .find(|(s, _, e)| *s == entry_id && e.type_id == CST_VALUE_OF)
            .map(|(_, t, _)| t);
        let Some(value_id) = value_id_opt else {
            continue;
        };
        if graph
            .get_node(&value_id)
            .is_none_or(|n| n.type_id != CST_MAPPING)
        {
            continue;
        }
        graph.set_node_attr(&value_id, cst_attr::CONSTRUCT, construct);
        synthesize_name_carrier(graph, value_id, construct, &key, s_start, s_end);
    }
}

/// Initial setup shared by every platform: create the cst:Document
/// node, find the root mapping, tag it `construct=pipeline`,
/// promote top-level carrier comments, and collect anchors.
/// Returns the parts every platform-specific dispatcher needs to
/// iterate root entries.
#[must_use]
pub fn open_pipeline<'doc>(doc: &'doc Document, source_file: &str) -> Option<PipelineSeed<'doc>> {
    let source = doc.source();
    let (mut graph, doc_id) = make_document(source_file, source);

    // A config file ALWAYS denotes exactly one pipeline — even an empty
    // one. The chaos walker can legitimately emit `{}` (an all-optional
    // root where every coin-flip came up false); our CST parser renders
    // `{}` as a Scalar, not a Mapping, so no Mapping child is found. Fall
    // back to the document root itself as the entry-iteration surrogate:
    // it carries no MappingEntry children, so zero entries are seeded, but
    // the empty pipeline mapping below is still created and round-trips
    // losslessly (empty pipeline -> `{}` -> empty pipeline).
    let top_mapping = doc
        .root()
        .children
        .iter()
        .find(|c| matches!(c.kind, NodeKind::Mapping))
        .unwrap_or_else(|| doc.root());

    let anchors = AnchorTable::collect(doc);
    let map_attrs = make_attrs(&[
        (cst_attr::SPAN_START, &top_mapping.span.start.to_string()),
        (cst_attr::SPAN_END, &top_mapping.span.end.to_string()),
    ]);
    let map_id = add_child(&mut graph, doc_id, CST_HAS_CHILD, CST_MAPPING, map_attrs);
    graph.set_node_attr(&map_id, cst_attr::CONSTRUCT, "pipeline");
    seed_carrier_comments(&mut graph, map_id, top_mapping, source);
    seed_carrier_comments(&mut graph, map_id, doc.root(), source);

    Some(PipelineSeed {
        graph,
        doc_id,
        map_id,
        top_mapping,
        anchors,
    })
}

/// Output of [`open_pipeline`] — the in-progress graph state that
/// a platform's dispatcher iterates over.
pub struct PipelineSeed<'doc> {
    pub graph: TypedGraph,
    pub doc_id: GhostId,
    pub map_id: GhostId,
    pub top_mapping: &'doc Node,
    pub anchors: AnchorTable<'doc>,
}

/// Iterate top-level entries with a closure. The closure is what
/// encodes the platform-specific "what is a job" logic.
pub fn for_each_top_entry<F>(seed: &PipelineSeed<'_>, mut f: F)
where
    F: FnMut(&Node, &str, &Node),
{
    for entry in &seed.top_mapping.children {
        if let NodeKind::MappingEntry { key_text } = &entry.kind {
            if entry.children.len() < 2 {
                continue;
            }
            f(entry, key_text, &entry.children[1]);
        }
    }
}

/// Convenience wrapper: open the pipeline, drive the closure over
/// each top-level entry, return the finished SeededGraph. For
/// platforms whose dispatch is purely per-entry. Platforms with
/// cross-entry coupling (e.g. needing the value_id of `jobs:`
/// after seeding it) should use the lower-level
/// [`open_pipeline`] / [`for_each_top_entry`] / finish manually.
pub fn seed_top_level<F>(
    doc: &Document,
    source_file: &str,
    classify: Classify<'_>,
    dispatch: F,
) -> SeededGraph
where
    F: FnMut(&mut TypedGraph, GhostId, &Node, &str, &Node, &str, &AnchorTable<'_>, Classify<'_>),
{
    seed_top_level_with_list_fields(doc, source_file, classify, &[], dispatch)
}

/// CST-level canonicalisation of `X | list<X>` construct fields: a single
/// mapping value of a list-canonical key is YAML sugar for a one-element
/// list, so wrap it in a synthetic `Sequence(SequenceItem(mapping))`. After
/// this pass the field ALWAYS presents as a sequence, so exactly ONE bijective
/// rule (seq_mapping_nodes) governs both directions — the ambiguous single
/// mapping_node shape (which collapsed N-item collections on reverse) is no
/// longer needed. The hub is identical either way (a collection); only the
/// CST surface is normalised, so NO IR data is invented. Scalars are left
/// untouched (their seq_attr/seq_scalar paths already handle single values).
/// Public entry: return a Document whose `X|list<X>` construct-field values
/// are canonicalised (single mapping → one-item sequence). Every platform
/// seeder calls this FIRST so the rest of seeding (whatever its structure)
/// sees the canonical list form — one bijective seq rule both directions.
/// No-op when `list_fields` is empty.
#[must_use]
pub fn canonicalize_document(doc: &Document, list_fields: &[&str]) -> Document {
    if list_fields.is_empty() {
        // Cheap clone-free path is impossible (Document owns its tree), but an
        // empty list means no wrapping — still must return an owned Document.
        return Document::from_parts(doc.source().to_string(), doc.root().clone());
    }
    Document::from_parts(
        doc.source().to_string(),
        canonicalize_list_fields(doc.root(), list_fields),
    )
}

fn canonicalize_list_fields(node: &Node, list_fields: &[&str]) -> Node {
    let mut out = node.clone();
    let mut new_children: Vec<Node> = node
        .children
        .iter()
        .map(|c| canonicalize_list_fields(c, list_fields))
        .collect();
    if let NodeKind::MappingEntry { key_text } = &node.kind {
        if list_fields.contains(&key_text.as_str())
            && new_children.len() >= 2
            && matches!(new_children[1].kind, NodeKind::Mapping)
        {
            let val = new_children[1].clone();
            let span = val.span;
            let item = Node {
                kind: NodeKind::SequenceItem,
                span,
                children: vec![val],
                anchor: None,
                tag: None,
            };
            new_children[1] = Node {
                kind: NodeKind::Sequence,
                span,
                children: vec![item],
                anchor: None,
                tag: None,
            };
        }
    }
    out.children = new_children;
    out
}

/// `seed_top_level` with the platform's list-canonical field keys. The doc is
/// canonicalised (single→one-item-sequence for those keys) before seeding.
pub fn seed_top_level_with_list_fields<F>(
    doc: &Document,
    source_file: &str,
    classify: Classify<'_>,
    list_fields: &[&str],
    mut dispatch: F,
) -> SeededGraph
where
    F: FnMut(&mut TypedGraph, GhostId, &Node, &str, &Node, &str, &AnchorTable<'_>, Classify<'_>),
{
    let canon_doc = canonicalize_document(doc, list_fields);
    let doc = &canon_doc;
    let Some(mut seed) = open_pipeline(doc, source_file) else {
        let source = doc.source();
        let (graph, doc_id) = make_document(source_file, source);
        return SeededGraph { graph, doc_id };
    };
    let source = doc.source();
    for entry in &seed.top_mapping.children {
        if let NodeKind::MappingEntry { key_text } = &entry.kind {
            if entry.children.len() < 2 {
                continue;
            }
            let value = &entry.children[1];
            dispatch(
                &mut seed.graph,
                seed.map_id,
                entry,
                key_text,
                value,
                source,
                &seed.anchors,
                classify,
            );
        }
    }
    SeededGraph {
        graph: seed.graph,
        doc_id: seed.doc_id,
    }
}

/// rc8 first-class re-architecture (satellites → first-class attrs):
/// lift each construct-tagged `cst:Mapping`'s SCALAR field entries onto the
/// mapping node as plain attributes (attr name = the entry key), so a field
/// rule can bind the value FIRST-CLASS onto the hub construct via the
/// construct correspondence — no `hub:attr` satellite, no `has_attr` edge.
///
/// Additive: the `cst:MappingEntry → cst:Scalar` children stay (they carry
/// per-entry provenance/comments); the lifted attribute is the value source
/// for the new first-class field rules. A "scalar field" = a construct
/// mapping entry whose value is a single `cst:Scalar`. Proven idiom:
/// `tests/first_class_attr.rs`.
pub fn lift_scalar_fields(graph: &mut TypedGraph) {
    let edges: Vec<(GhostId, GhostId, String)> = graph
        .iter_edges()
        .into_iter()
        .map(|(s, t, e)| (s, t, e.type_id.clone()))
        .collect();
    let constructs: Vec<GhostId> = graph
        .iter_nodes()
        .filter(|n| n.type_id == CST_MAPPING && n.attrs.contains_key(cst_attr::CONSTRUCT))
        .map(|n| n.id)
        .collect();
    let mut lifts: Vec<(GhostId, String, String)> = Vec::new();
    for mc in &constructs {
        for (s, entry_id, ek) in &edges {
            if s != mc || ek != CST_HAS_CHILD {
                continue;
            }
            let key = match graph.get_node(entry_id) {
                Some(n) if n.type_id == CST_MAPPING_ENTRY => match n.attrs.get(cst_attr::KEY) {
                    Some(k) => k.clone(),
                    None => continue,
                },
                _ => continue,
            };
            for (s2, sc_id, ek2) in &edges {
                if s2 != entry_id || ek2 != CST_VALUE_OF {
                    continue;
                }
                if let Some(sc) = graph.get_node(sc_id) {
                    if sc.type_id == CST_SCALAR {
                        if let Some(text) = sc.attrs.get(cst_attr::TEXT) {
                            lifts.push((*mc, key.clone(), text.clone()));
                        }
                    }
                }
            }
        }
    }
    for (mc, key, val) in lifts {
        graph.set_node_attr(&mc, &key, &val);
    }
}
