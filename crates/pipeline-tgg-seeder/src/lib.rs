//! Catalog-driven CST → seesaw `TypedGraph` seeder.
//!
//! Shared primitives across every YAML platform's TGG bridge:
//! recursive walk, construct-tagging from a per-platform
//! classification table, carrier-comment promotion, scalar
//! resolution, anchor/merge resolution.
//!
//! What's *not* here: the top-level dispatch — i.e. how to walk
//! the root mapping and decide which entries are jobs vs meta vs
//! pipeline-level. That's platform-specific and lives in each
//! per-platform crate's `seed.rs`, calling into the primitives
//! below.

